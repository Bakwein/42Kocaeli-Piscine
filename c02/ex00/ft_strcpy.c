#include <stdio.h>
#include <string.h>

char *ft_strcpy(char *dest, char *src)
{
	int	index;

	index = 0;
	while (src[index] != '\0')
	{
		dest[index] = src[index];
		index++;
	}
	dest[index] = '\0';
	return (dest);
}

int	main(void)
{
	char	*src;
	char	dest[509];

	src = "42 Kocaeli";
	printf("base : %s\n", src);	
	ft_strcpy(dest, src);
	printf("ft_cpy : %s\n", dest);
}
